function onKill(cid, target)
        local monster = monsters[getCreatureName(target):lower()]
        if(isPlayer(target) == FALSE and monster) then
           
local pokemon = getPlayerSlotItem(cid, 8)
local atributo = getItemAttribute(pokemon.uid, "exp")
local nivel = getItemAttribute(pokemon.uid, "boost")
local calc = nivel*2000*1.1
local exp = monster+atributo
local monstro = getCreatureSummons(cid)[1]


doItemSetAttribute(pokemon.uid, "exp", exp)

doSendAnimatedText(getCreaturePosition(monstro), ""..monster.."", 212)
if exp > calc then
local nomedopoke = getCreatureName(monstro)
local advanced = nivel+1
local description = "Contains a "..nomedopoke.." [+"..advanced.."]."
local treinador = getPlayerName(cid)
doItemSetAttribute(pokemon.uid, "exp", "0")
doItemSetAttribute(pokemon.uid, "boost", advanced)
doItemSetAttribute(pokemon.uid, "description", description)
doCreatureSay(monstro,  "trainer "..treinador.." ,i'm advanced from level "..nivel.." to "..advanced..".", 1)

end

         
               

  end   
return TRUE      

end