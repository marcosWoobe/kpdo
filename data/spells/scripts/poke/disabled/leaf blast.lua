function onCastSpell(cid, var)
if getPlayerStorageValue(cid, 8) >= 1 then
	return true
end
	doCreatureSay(cid, "LEAF BLADE!", TALKTYPE_MONSTER)
	if getPlayerStorageValue(cid, 3) >= 1 then
	doSendAnimatedText(getThingPos(cid), "MISS", 215)
	setPlayerStorageValue(cid, 3, -1)
	return true
	end
	if getPlayerStorageValue(cid, 5) >= 1 then
		if math.random(1,100) <= 33 then
		doSendAnimatedText(getThingPos(cid), "SELF HIT", 180)
			if isPlayer(getCreatureTarget(cid)) then
			huah = getPlayerLevel(getCreatureTarget(cid))
			else
			huah = getPlayerLevel(getCreatureMaster(getCreatureTarget(cid)))
			end
		local levels = huah
		doTargetCombatHealth(getCreatureTarget(cid), cid, COMBAT_PHYSICALDAMAGE, -(math.random((levels*3),(levels*5))), -((math.random((levels*3),(levels*5))+10)), 79)
		return true
		end
	end
if getCreatureName(cid) == "Bulbasaur" then
yyy = 100
elseif getCreatureName(cid) == "Ivysaur" then
yyy = 200
elseif getCreatureName(cid) == "Venusaur" then
yyy = 2000
end
local y = yyy
		doAreaCombatHealth(cid, GRASSDAMAGE, getThingPos(cid), rageblast1, -(y), -(y+20), 79)
		local function damage(params)
		if isCreature(params.cid) then
		doAreaCombatHealth(cid, GRASSDAMAGE, getThingPos(cid), params.x, -(y), -(y+20), 79)
		end
		end
		addEvent(damage, 200, {cid = cid, x = rageblast2})
		addEvent(damage, 400, {cid = cid, x = rageblast3})
		addEvent(damage, 600, {cid = cid, x = rageblast4})
end
